package fernuni.propra.file_processing;

public class UserReadInputWriteOutputException extends Exception {
	public UserReadInputWriteOutputException() {
		super();
	}

	public UserReadInputWriteOutputException(String message, Throwable cause) {
		super(message, cause);
	}

	public UserReadInputWriteOutputException(String message) {
		super(message);
	}

	public UserReadInputWriteOutputException(Throwable cause) {
		super(cause);
	}

}
