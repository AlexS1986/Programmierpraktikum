package fernuni.propra.user_interface;

public class RoomPanelException extends Exception {

	public RoomPanelException() {
	}

	public RoomPanelException(String message) {
		super(message);
	}

	public RoomPanelException(Throwable cause) {
		super(cause);
	}

	public RoomPanelException(String message, Throwable cause) {
		super(message, cause);
	}


}
